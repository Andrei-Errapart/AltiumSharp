namespace OriginalCircuit.Altium.Models.Pcb;

/// <summary>
/// Represents a PCB object class from the <c>Classes6</c> storage (groups of nets, components, etc.).
/// Fully typed so it round-trips byte-for-byte and is authored from scratch without replaying the raw
/// parameter block.
/// </summary>
public sealed class PcbObjectClass
{
    // Common primitive parameter prefix.
    /// <summary>Selection flag (transient; FALSE on disk).</summary>
    public bool Selection { get; set; }
    /// <summary>Layer token (typically <c>MULTILAYER</c>).</summary>
    public string Layer { get; set; } = "MULTILAYER";
    /// <summary>Whether the class is locked.</summary>
    public bool Locked { get; set; }
    /// <summary>Polygon-outline flag.</summary>
    public bool PolygonOutline { get; set; }
    /// <summary>User-routed flag.</summary>
    public bool UserRouted { get; set; } = true;
    /// <summary>Keepout flag.</summary>
    public bool Keepout { get; set; }
    /// <summary>Union index.</summary>
    public int UnionIndex { get; set; }

    /// <summary>Class name.</summary>
    public string Name { get; set; } = string.Empty;

    /// <summary>
    /// Class kind, stored verbatim as written on disk (an integer enum token such as <c>1</c> for net
    /// classes; from-scratch callers may also use descriptive text).
    /// </summary>
    public string Kind { get; set; } = string.Empty;

    /// <summary>Whether this is a built-in "super" class (the <c>SUPERCLASS</c> flag).</summary>
    public bool SuperClass { get; set; }

    /// <summary>Member names in this class (the <c>M0</c>, <c>M1</c>, … keys).</summary>
    public List<string> Members { get; } = new();

    /// <summary>Selected flag (transient; FALSE on disk).</summary>
    public bool Selected { get; set; }

    /// <summary>Whether this is an auto-generated class (the <c>AUTOGENERATEDCLASS</c> flag); null when absent.</summary>
    public bool? AutoGeneratedClass { get; set; }

    /// <summary>Whether the class is a schematic auto-generated cluster.</summary>
    public bool SchAutoGeneratedCluster { get; set; }

    /// <summary>Unique 8-character identity token; minted fresh for new classes, round-tripped otherwise.</summary>
    public string UniqueId { get; set; } = string.Empty;

    /// <summary>Auto-generated class kind (the trailing <c>AUTOGENERATEDCLASSKIND</c> key); null when absent.</summary>
    public int? AutoGeneratedClassKind { get; set; }
}
