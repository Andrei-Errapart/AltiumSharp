namespace OriginalCircuit.Altium.Models.Pcb;

/// <summary>
/// A PcbDoc <c>SignalClasses</c> record (an xSignal / net class). Same record-framed parameter block as
/// <c>Classes6</c>. Fully typed so it round-trips byte-for-byte without replaying the raw block.
/// See docs/decompile/feature-signal-classes.md.
/// </summary>
public sealed class PcbSignalClass
{
    // Common primitive parameter prefix.
    /// <summary>Selection flag (transient; FALSE on disk).</summary>
    public bool Selection { get; set; }
    /// <summary>Layer token (typically <c>MULTILAYER</c>).</summary>
    public string Layer { get; set; } = "MULTILAYER";
    /// <summary>Whether the class is locked.</summary>
    public bool Locked { get; set; }
    /// <summary>Polygon-outline flag.</summary>
    public bool PolygonOutline { get; set; }
    /// <summary>User-routed flag.</summary>
    public bool UserRouted { get; set; } = true;
    /// <summary>Keepout flag.</summary>
    public bool Keepout { get; set; }
    /// <summary>Union index.</summary>
    public int UnionIndex { get; set; }

    /// <summary>Class name (e.g. "All xSignals").</summary>
    public string Name { get; set; } = "All xSignals";

    /// <summary>Class kind (default 10).</summary>
    public int Kind { get; set; } = 10;

    /// <summary>Whether this is a super-class (default true).</summary>
    public bool SuperClass { get; set; } = true;

    /// <summary>Selected flag (transient; FALSE on disk).</summary>
    public bool Selected { get; set; }

    /// <summary>Whether the class is a schematic auto-generated cluster.</summary>
    public bool SchAutoGeneratedCluster { get; set; }

    /// <summary>Unique id token.</summary>
    public string? UniqueId { get; set; }
}
