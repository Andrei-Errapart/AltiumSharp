    private PcbDocument Read(CompoundFileAccessor accessor, CancellationToken cancellationToken = default)
    {
        _diagnostics = new List<AltiumDiagnostic>();
        var document = new PcbDocument();

        // The legacy FileHeader stream is a fixed 24-byte constant stamp (no per-document data); it is
        // fully modeled and regenerated by PcbDocWriter, so nothing needs to be captured here.
        ReadFileHeaderSix(accessor, document);

        // Read wide strings (used by text primitives)
        var wideStrings = ReadWideStrings(accessor);

        // Read board-level parameters
        ReadBoard(accessor, document);
        ReadNets(accessor, document);

        cancellationToken.ThrowIfCancellationRequested();

        // Read each primitive type from its dedicated storage
        ReadArcs(accessor, document, cancellationToken);
        ReadPads(accessor, document, cancellationToken);
        ReadVias(accessor, document, cancellationToken);
        ReadTracks(accessor, document, cancellationToken);
        ReadTexts(accessor, document, wideStrings, cancellationToken);
        ReadFills(accessor, document, cancellationToken);
        ReadRegions(accessor, document, cancellationToken);
        ReadBoardRegions(accessor, document, cancellationToken);
        ReadShapeBased(accessor, "ShapeBasedRegions6", 0x0B, document.ShapeBasedRegions);
        ReadShapeBased(accessor, "ShapeBasedComponentBodies6", 0x0C, document.ShapeBasedComponentBodies);
        ReadPrimitiveParameters(accessor, document);
        ReadExtendedPrimitiveInformation(accessor, document);
        ReadNamedParameterStorages(accessor, document);
        ReadEmbeddedModels(accessor, document);
        ReadDocumentPrimitiveGuids(accessor, document);
        ReadDocumentPrimitiveUniqueIds(accessor, document);
        var fviStorage = accessor.TryGetStorage("FileVersionInfo");
        if (fviStorage != null && fviStorage.TryGetStream("Data", out var fviData))
            document.FileVersionInfo = PcbLibReader.ParseFileVersionInfo(fviData.GetData());
        var lkmStorage = accessor.TryGetStorage("LayerKindMapping");
        if (lkmStorage != null && lkmStorage.TryGetStream("Data", out var lkmData))
            document.LayerKindMapping = PcbLibReader.ParseLayerKindMapping(lkmData.GetData());
        document.PadViaLibrary = PcbLibReader.ParsePadViaLibrary(accessor.RootStorage, "PadViaLibrary");
        document.PadViaLibraryCache = PcbLibReader.ParsePadViaLibrary(accessor.RootStorage, "PadViaLibraryCache");
        ReadComponentBodies(accessor, document, cancellationToken);
        ReadPolygons(accessor, document, cancellationToken);
        ReadComponents(accessor, document, cancellationToken);
        ReadEmbeddedBoards(accessor, document, cancellationToken);

        // Assign document-level pads to their parent components using the binary component index
        AssignPadsToComponents(document);

        // Read parameter-block storages
        ReadRules(accessor, document);
        ReadClasses(accessor, document);
        ReadSignalClasses(accessor, document);
        ReadSmartUnions(accessor, document);
        ReadUnionNames(accessor, document);
        ReadDifferentialPairs(accessor, document);
        ReadRooms(accessor, document);

        // Capture unknown storages for round-trip fidelity
        ReadAdditionalStreams(accessor, document);

        document.Diagnostics = _diagnostics;
        return document;
    }

    private static void ReadFileHeaderSix(CompoundFileAccessor accessor, PcbDocument document)
    {
        // FileHeaderSix carries the modern version stamp + per-document GUID (fileheaders.md §4).
        // Model the GUID; everything else is a Reserved constant. Null when the stream is absent so
        // the writer reproduces "no FileHeaderSix" exactly (round-trip) while a from-scratch document
        // keeps its default fresh GUID.
        var stream = accessor.TryGetStream("FileHeaderSix");
        if (stream == null)
        {
            document.FileGuid = null;
            return;
        }

        var data = stream.GetData();
        using var ms = new MemoryStream(data);
        using var reader = new BinaryFormatReader(ms, leaveOpen: true);

        var versionBlockLen = reader.ReadInt32();           // [u32 len][b len][version text] — Reserved
        if (versionBlockLen <= 0) { document.FileGuid = null; return; }
        reader.Skip(reader.ReadByte());
        if (!reader.HasMore) { document.FileGuid = null; return; }
        reader.Skip(8);                                     // 5.01 double — Reserved

        if (!reader.HasMore) { document.FileGuid = null; return; }
        var guidBlockLen = reader.ReadInt32();              // [u32 len][b len][{GUID}] — Identity
        if (guidBlockLen <= 0) { document.FileGuid = null; return; }
        var guidLen = reader.ReadByte();
        var guidBytes = new byte[guidLen];
        reader.ReadExact(guidBytes);
        var guidText = AltiumEncoding.Windows1252.GetString(guidBytes);
        document.FileGuid = Guid.TryParse(guidText, out var g) ? g : null;
    }

    private void ReadAdditionalStreams(CompoundFileAccessor accessor, PcbDocument document)
    {
        var additional = new Dictionary<string, byte[]>(StringComparer.OrdinalIgnoreCase);

        foreach (var item in accessor.EnumerateChildren(accessor.RootStorage))
        {
            // Record every root entry so the writer can reproduce known storages that were
            // present but empty (e.g. DifferentialPairs6) instead of omitting them.
            if (item.IsStorage)
                document.PresentStorages.Add(item.Name);

            if (KnownStorageNames.Contains(item.Name))
                continue;

            if (item.IsStorage)
            {
                var childStorage = item.AsStorage();
                foreach (var stream in accessor.EnumerateStreams(childStorage))
                {
                    try
                    {
                        additional[$"{item.Name}/{stream.Name}"] = stream.GetData();
                    }
                    catch (Exception ex) when (ex is EndOfStreamException or InvalidDataException or IOException)
                    {
                        _diagnostics.Add(new AltiumDiagnostic(DiagnosticSeverity.Warning, $"Failed to read stream '{item.Name}/{stream.Name}': {ex.Message}"));
                    }
                }
            }
            else
            {
                var rootStream = item.AsStream();
                try
                {
                    additional[item.Name] = rootStream.GetData();
                }
                catch (Exception ex) when (ex is EndOfStreamException or InvalidDataException or IOException)
                {
                    _diagnostics.Add(new AltiumDiagnostic(DiagnosticSeverity.Warning, $"Failed to read stream '{item.Name}': {ex.Message}"));
                }
            }
        }

        if (additional.Count > 0)
            document.AdditionalStreams = additional;
    }

    private static List<string> ReadWideStrings(CompoundFileAccessor accessor)
    {
        var result = new List<string>();
        var storage = accessor.TryGetStorage("WideStrings6");
        if (storage == null)
            return result;

        var dataStream = PcbLibReader.GetChildStream(storage, "Data");
        if (dataStream == null)
            return result;

        var data = dataStream.GetData();

        // WideStrings6 is a sequence of [u32 text_index][u32 byte_len][UTF-16LE string incl. null].
        var pos = 0;
        while (pos + 8 <= data.Length)
        {
            var index = BitConverter.ToInt32(data, pos);
            var byteLen = BitConverter.ToInt32(data, pos + 4);
            pos += 8;
            if (byteLen < 0 || pos + byteLen > data.Length)
                break;
            var s = System.Text.Encoding.Unicode.GetString(data, pos, byteLen);
            pos += byteLen;
            var nul = s.IndexOf('\0');
            if (nul >= 0)
                s = s.Substring(0, nul);
            if (index < 0)
                continue;
            while (result.Count <= index)
                result.Add(string.Empty);
            result[index] = s;
        }

        return result;
    }

    private static void ReadBoard(CompoundFileAccessor accessor, PcbDocument document)
    {
        var storage = accessor.TryGetStorage("Board6");
        if (storage == null)
            return;

        var dataStream = PcbLibReader.GetChildStream(storage, "Data");
        if (dataStream == null)
            return;

        var data = dataStream.GetData();
        if (data.Length == 0)
            return;

        using var ms = new MemoryStream(data);
        using var reader = new BinaryFormatReader(ms, leaveOpen: true);

        var parameters = PcbLibReader.ReadParameterBlock(reader, out var rawBoard);
        if (parameters.Count > 0)
        {
            document.BoardParameters = parameters;
            document.BoardParametersOrdered = PcbLibReader.ParseParametersOrdered(rawBoard);
        }
    }

    private static void ReadNets(CompoundFileAccessor accessor, PcbDocument document)
    {
        var storage = accessor.TryGetStorage("Nets6");
        if (storage == null)
            return;

        var dataStream = PcbLibReader.GetChildStream(storage, "Data");
        if (dataStream == null)
            return;

        var data = dataStream.GetData();
        if (data.Length == 0)
            return;

        using var ms = new MemoryStream(data);
        using var reader = new BinaryFormatReader(ms, leaveOpen: true);

        while (reader.HasMore)
        {
            var parameters = PcbLibReader.ReadParameterBlock(reader, out var rawNet);
            if (parameters.Count == 0)
                continue;

            document.AddNet(ParseNet(PcbLibReader.ParseParametersOrdered(rawNet)));
        }
    }

    // Parses a Nets6 record from its ordered parameter list into a fully typed PcbNet, so the writer
    // can regenerate the exact bytes without replaying the captured parameter block.
    private static PcbNet ParseNet(List<KeyValuePair<string, string>> ordered)
    {
        var net = new PcbNet();
        foreach (var (key, value) in ordered)
        {
            switch (key.ToUpperInvariant())
            {
                case "SELECTION": net.Selection = ParamBool(value); break;
                case "LAYER": net.Layer = value; break;
                case "LOCKED": net.Locked = ParamBool(value); break;
                case "POLYGONOUTLINE": net.PolygonOutline = ParamBool(value); break;
                case "USERROUTED": net.UserRouted = ParamBool(value); break;
                case "KEEPOUT": net.Keepout = ParamBool(value); break;
                case "UNIONINDEX": net.UnionIndex = ParamInt(value); break;
                case "PRIMITIVELOCK": net.PrimitiveLock = ParamBool(value); break;
                case "NAME": net.Name = value; break;
                case "VISIBLE": net.Visible = ParamBool(value); break;
                case "COLOR": net.Color = ParamInt(value); break;
                case "LOOPREMOVAL": net.LoopRemoval = ParamBool(value); break;
                case "OVERRIDECOLORFORDRAW": net.OverrideColorForDraw = ParamBool(value); break;
                case "UNIQUEID": net.UniqueId = value; break;
                case "JUMPERSVISIBLE": net.JumpersVisible = ParamBool(value); break;
                case "ROUTEDLENGTH": net.RoutedLength = ParamInt(value); break;
                case "MANHATTANLENGTH": net.ManhattanLength = ParamInt(value); break;
                case "TARGETLENGTH": net.TargetLengthUnits = ParamMilUnits(value); break;
                case "MRVIASIZE": net.MinRoutedViaSizeUnits = ParamMilUnits(value); break;
                case "MRVIAHOLE": net.MinRoutedViaHoleUnits = ParamMilUnits(value); break;
                case "SIGNALLENGTH": net.SignalLength = ParamInt(value); break;
                case "SIGNALDELAY": net.SignalDelay = ParamDouble(value); break;
                case "DELAYTOTAL": net.DelayTotal = ParamDouble(value); break;
                case "CURRENTTOTAL": net.CurrentTotal = ParamDouble(value); break;
                case "RESISTANCETOTAL": net.ResistanceTotal = ParamDouble(value); break;
                default:
                    if (key.EndsWith("_MRWIDTH", StringComparison.OrdinalIgnoreCase))
                    {
                        net.LayerMinRoutedWidths ??= new List<PcbNetLayerWidth>();
                        net.LayerMinRoutedWidths.Add(new PcbNetLayerWidth
                        {
                            LayerKey = key[..^"_MRWIDTH".Length],
                            WidthUnits = ParamMilUnits(value),
                        });
                    }
                    break;
            }
        }
        return net;
    }

    private static bool ParamBool(string value) => value.Equals("TRUE", StringComparison.OrdinalIgnoreCase);

    private static int ParamInt(string value) =>
        int.TryParse(value, NumberStyles.Integer, CultureInfo.InvariantCulture, out var v) ? v : 0;

    private static double ParamDouble(string value) =>
        double.TryParse(value, NumberStyles.Float, CultureInfo.InvariantCulture, out var v) ? v : 0;

    // Parses a width token like "11.811mil" into internal coordinate units (1 mil = 10000), rounding
    // to the nearest unit (Coord.FromMils truncates, which would lose the last unit on round-trip).
    private static int ParamMilUnits(string value)
    {
        var s = value.EndsWith("mil", StringComparison.OrdinalIgnoreCase) ? value[..^3] : value;
        return double.TryParse(s, NumberStyles.Float, CultureInfo.InvariantCulture, out var mils)
            ? (int)Math.Round(mils * 10000.0)
            : 0;
    }

    // Parses a mil token (e.g. "1338.5827mil") into a Coord via rounded raw units.
    private static Coord ParamMilCoord(string value) => Coord.FromRaw(ParamMilUnits(value));

    // Decodes the UNICODE=EXISTS wrapping: a param block with non-Latin-1 text carries the full
    // value as a UNICODE__<KEY>=<comma-decimal UTF-16 code units> companion (the inline value has the
    // >255 codepoints stripped). Replace each base key with its true decoded value and drop the
    // UNICODE markers, so downstream typed parsing sees the real strings.
    internal static void DecodeUnicodeParameters(Dictionary<string, string> p)
    {
        if (!p.TryGetValue("UNICODE", out var flag) || !flag.Equals("EXISTS", StringComparison.OrdinalIgnoreCase))
            return;
        var remove = new List<string> { "UNICODE" };
        foreach (var kvp in p)
        {
            if (!kvp.Key.StartsWith("UNICODE__", StringComparison.OrdinalIgnoreCase))
                continue;
            remove.Add(kvp.Key);
            var baseKey = kvp.Key.Substring("UNICODE__".Length);
            var sb = new System.Text.StringBuilder();
            foreach (var code in kvp.Value.Split(','))
                if (int.TryParse(code, NumberStyles.Integer, CultureInfo.InvariantCulture, out var cu))
                    sb.Append((char)cu);
            p[baseKey] = sb.ToString();
        }
        foreach (var k in remove)
            p.Remove(k);
    }

    private void ReadParameterBlockStorage(CompoundFileAccessor accessor, string storageName,
        Action<Dictionary<string, string>, List<KeyValuePair<string, string>>> addItem)
    {
        var storage = accessor.TryGetStorage(storageName);
        if (storage == null)
            return;

        var dataStream = PcbLibReader.GetChildStream(storage, "Data");
        if (dataStream == null)
            return;

        var data = dataStream.GetData();
        if (data.Length == 0)
            return;

        try
        {
            using var ms = new MemoryStream(data);
            using var reader = new BinaryFormatReader(ms, leaveOpen: true);

            while (reader.HasMore)
            {
                var parameters = PcbLibReader.ReadParameterBlock(reader, out var raw);
                if (parameters.Count == 0)
                    continue;

                addItem(parameters, PcbLibReader.ParseParametersOrdered(raw));
            }
        }
        catch (Exception ex) when (ex is EndOfStreamException or InvalidDataException or FormatException or OverflowException)
        {
            _diagnostics.Add(new AltiumDiagnostic(DiagnosticSeverity.Warning,
                $"Failed to fully parse {storageName}: {ex.Message}", storageName));
        }
    }

    private void ReadRules(CompoundFileAccessor accessor, PcbDocument document)
    {
        var storage = accessor.TryGetStorage("Rules6");
        if (storage == null)
            return;
        var dataStream = PcbLibReader.GetChildStream(storage, "Data");
        if (dataStream == null)
            return;
        var data = dataStream.GetData();

        // Rules6 records use a 2-byte leader + 4-byte length + null-terminated text layout,
        // unlike Nets6/Classes6 which omit the leader. The leader varies per record and must
        // be preserved for round-trip.
        var pos = 0;
        while (pos + 6 <= data.Length)
        {
            var leader = (ushort)(data[pos] | (data[pos + 1] << 8));
            var length = BitConverter.ToInt32(data, pos + 2);
            pos += 6;
            if (length <= 0 || pos + length > data.Length)
                break;

            var nul = Array.IndexOf(data, (byte)0, pos, length);
            var textLen = nul >= 0 ? nul - pos : length;
            var text = AltiumEncoding.Windows1252.GetString(data, pos, textLen);
            pos += length;

            var parameters = PcbLibReader.ParseParameters(text);
            DecodeUnicodeParameters(parameters);
            var ruleKind = parameters.TryGetValue("RULEKIND", out var rk) ? rk : string.Empty;
            var rule = CreateRuleForKind(ruleKind);
            rule.RawLeader = leader;
            rule.Parameters = parameters;
            rule.ReadCommonHeader(parameters);
            if (rule.IsModeled)
            {
                rule.ReadBody(parameters);
                rule.ReadOrdered(PcbLibReader.ParseParametersOrdered(text));
            }
            else
                rule.RawParametersOrdered = PcbLibReader.ParseParametersOrdered(text); // kind not yet typed
            document.AddRule(rule);
        }
    }

    // Maps a RULEKIND to its typed rule class. Kinds with dynamic per-layer key blocks (Width,
    // DiffPairsRouting, RoutingLayers) and any unknown kind fall back to the base PcbRule (replay).
    private static PcbRule CreateRuleForKind(string kind) => kind switch
    {
        "Clearance" or "BoardOutlineClearance" => new PcbClearanceRule(),
        "ComponentClearance" => new PcbComponentClearanceRule(),
        "AssemblyTestpoint" => new PcbAssemblyTestpointRule(),
        "FabricationTestpoint" => new PcbFabricationTestpointRule(),
        "AssemblyTestPointUsage" or "SilkToBoardRegionClearance" or "UnpouredPolygon" or "UnRoutedNet" => new PcbHeaderOnlyRule(),
        "FabricationTestPointUsage" => new PcbFabricationTestPointUsageRule(),
        "FanoutControl" => new PcbFanoutControlRule(),
        "Height" => new PcbHeightRule(),
        "HoleSize" => new PcbHoleSizeRule(),
        "HoleToHoleClearance" => new PcbHoleToHoleClearanceRule(),
        "LayerPairs" => new PcbLayerPairsRule(),
        "Length" => new PcbLengthRule(),
        "MatchedLengths" => new PcbMatchedLengthsRule(),
        "MinimumSolderMaskSliver" => new PcbMinimumSolderMaskSliverRule(),
        "NetAntennae" => new PcbNetAntennaeRule(),
        "PasteMaskExpansion" => new PcbPasteMaskExpansionRule(),
        "PlaneClearance" => new PcbPlaneClearanceRule(),
        "PlaneConnect" => new PcbPlaneConnectRule(),
        "PolygonConnect" => new PcbPolygonConnectRule(),
        "RoutingCorners" => new PcbRoutingCornersRule(),
        "RoutingLayers" => new PcbRoutingLayersRule(),
        "RoutingPriority" => new PcbRoutingPriorityRule(),
        "RoutingTopology" => new PcbRoutingTopologyRule(),
        "RoutingVias" => new PcbRoutingViasRule(),
        "ShortCircuit" => new PcbShortCircuitRule(),
        "SignalStimulus" => new PcbSignalStimulusRule(),
        "SilkToSilkClearance" => new PcbSilkToSilkClearanceRule(),
        "SilkToSolderMaskClearance" => new PcbSilkToSolderMaskClearanceRule(),
        "SolderMaskExpansion" => new PcbSolderMaskExpansionRule(),
        "SupplyNets" => new PcbSupplyNetsRule(),
        _ => new PcbRule(),
    };

    private void ReadClasses(CompoundFileAccessor accessor, PcbDocument document)
    {
        ReadParameterBlockStorage(accessor, "Classes6", (_, ordered) => document.AddClass(ParseObjectClass(ordered)));
    }

    // Parses a Classes6 record from its ordered parameter list into a fully typed PcbObjectClass.
    private static PcbObjectClass ParseObjectClass(List<KeyValuePair<string, string>> ordered)
    {
        var oc = new PcbObjectClass();
        foreach (var (key, value) in ordered)
        {
            switch (key.ToUpperInvariant())
            {
                case "SELECTION": oc.Selection = ParamBool(value); break;
                case "LAYER": oc.Layer = value; break;
                case "LOCKED": oc.Locked = ParamBool(value); break;
                case "POLYGONOUTLINE": oc.PolygonOutline = ParamBool(value); break;
                case "USERROUTED": oc.UserRouted = ParamBool(value); break;
                case "KEEPOUT": oc.Keepout = ParamBool(value); break;
                case "UNIONINDEX": oc.UnionIndex = ParamInt(value); break;
                case "NAME": oc.Name = value; break;
                case "KIND": oc.Kind = value; break;
                case "SUPERCLASS": oc.SuperClass = ParamBool(value); break;
                case "AUTOGENERATEDCLASS": oc.AutoGeneratedClass = ParamBool(value); break;
                case "SELECTED": oc.Selected = ParamBool(value); break;
                case "SCHAUTOGENERATEDCLUSTER": oc.SchAutoGeneratedCluster = ParamBool(value); break;
                case "UNIQUEID": oc.UniqueId = value; break;
                case "AUTOGENERATEDCLASSKIND": oc.AutoGeneratedClassKind = ParamInt(value); break;
                default:
                    if (key.Length > 1 && (key[0] == 'M' || key[0] == 'm') && char.IsDigit(key[1]))
                        oc.Members.Add(value);
                    break;
            }
        }
        return oc;
    }

